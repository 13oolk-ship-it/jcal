@Injectable({ providedIn: 'root' })
export class I18nService {
  private _lang = signal<Lang>('en');

  readonly lang = this._lang.asReadonly();

  t(key: string): string {
    return translations[this._lang()]?.[key] ?? key;
  }

  setLang(lang: Lang) {
    this._lang.set(lang);
  }
}
